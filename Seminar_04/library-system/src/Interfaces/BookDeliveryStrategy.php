<?php

namespace LibrarySystem\Interfaces;

/**
 * Интерфейс стратегии доставки книг
 * 
 * Определяет общий интерфейс для всех стратегий доставки книг.
 * Каждая стратегия реализует свой способ доставки (цифровая, бумажная и т.д.)
 */
interface BookDeliveryStrategy {
    /**
     * Выполняет доставку книги в соответствии с выбранной стратегией
     * 
     * @return string Информация о способе получения книги
     */
    public function deliver(): string;
}
